<script>
    let testimonials = document.querySelectorAll('.testimonial');
    let currentIndex = 0;

    function showNextTestimonial() {
        let currentTestimonial = testimonials[currentIndex];
        let nextIndex = (currentIndex + 1) % testimonials.length;
        let nextTestimonial = testimonials[nextIndex];

        // Hilangkan testimoni saat ini dengan efek fade-out dan geser ke kiri
        currentTestimonial.classList.remove('opacity-100', 'translate-x-0');
        currentTestimonial.classList.add('opacity-0', '-translate-x-full');

        // Munculkan testimoni berikutnya dengan efek fade-in dan geser dari kanan
        nextTestimonial.classList.remove('opacity-0', 'translate-x-full');
        nextTestimonial.classList.add('opacity-100', 'translate-x-0');

        // Update index testimoni
        currentIndex = nextIndex;

        // Reset posisi agar testimoni bisa diputar ulang dengan benar
        setTimeout(() => {
            currentTestimonial.classList.add('translate-x-full');
            currentTestimonial.classList.remove('-translate-x-full');
        }, 700);
    }

    // Ganti testimoni setiap 5 detik
    setInterval(showNextTestimonial, 5000);
</script>
